// Auth Events
abstract class AuthEvent extends Equatable {
  @override
  List<Object> get props => [];
}

class LoginEvent extends AuthEvent {
  final String email;
  final String password;

  LoginEvent({required this.email, required this.password});

  @override
  List<Object> get props => [email, password];
}

class SignUpEvent extends AuthEvent {
  final String email;
  final String password;
  final String fullName;
  final String? location;
  final String? phoneNumber;
  final String? countryCode;

  SignUpEvent({
    required this.email,
    required this.password,
    required this.fullName,
    this.location,
    this.phoneNumber,
    this.countryCode,
  });

  @override
  List<Object> get props => [email, password, fullName];
}

class ForgotPasswordEvent extends AuthEvent {
  final String email;

  ForgotPasswordEvent({required this.email});

  @override
  List<Object> get props => [email];
}

class ChangePasswordEvent extends AuthEvent {
  final String currentPassword;
  final String newPassword;

  ChangePasswordEvent({
    required this.currentPassword,
    required this.newPassword,
  });

  @override
  List<Object> get props => [currentPassword, newPassword];
}

class LogoutEvent extends AuthEvent {}

class CheckAuthEvent extends AuthEvent {}

// OTP Events
class SendEmailOTPEvent extends AuthEvent {
  final String email;

  SendEmailOTPEvent({required this.email});

  @override
  List<Object> get props => [email];
}

class VerifyEmailOTPEvent extends AuthEvent {
  final String email;
  final String otp;
  final String password;
  final String? fullName;
  final String? location;
  final String? phoneNumber;
  final String? countryCode;

  VerifyEmailOTPEvent({
    required this.email,
    required this.otp,
    required this.password,
    this.fullName,
    this.location,
    this.phoneNumber,
    this.countryCode,
  });

  @override
  List<Object> get props => [
        email,
        otp,
        password,
        fullName ?? '',
        location ?? '',
        phoneNumber ?? '',
        countryCode ?? ''
      ];
}

class ResendEmailOTPEvent extends AuthEvent {
  final String email;

  ResendEmailOTPEvent({required this.email});

  @override
  List<Object> get props => [email];
}

// Auth States
abstract class AuthState extends Equatable {
  @override
  List<Object> get props => [];
}

class AuthInitial extends AuthState {}

class AuthLoading extends AuthState {}

class AuthAuthenticated extends AuthState {
  final String userId;
  final String email;

  AuthAuthenticated({required this.userId, required this.email});

  @override
  List<Object> get props => [userId, email];
}

class AuthUnauthenticated extends AuthState {}

class AuthError extends AuthState {
  final String message;

  AuthError({required this.message});

  @override
  List<Object> get props => [message];
}

class AuthSuccess extends AuthState {
  final String message;

  AuthSuccess({required this.message});

  @override
  List<Object> get props => [message];
}

// OTP States
class EmailOTPSent extends AuthState {
  final String email;
  final String message;

  EmailOTPSent({required this.email, required this.message});

  @override
  List<Object> get props => [email, message];
}

class EmailOTPVerified extends AuthState {
  final String userId;
  final String email;
  final String message;

  EmailOTPVerified(
      {required this.userId, required this.email, required this.message});

  @override
  List<Object> get props => [userId, email, message];
}

class EmailOTPError extends AuthState {
  final String message;

  EmailOTPError({required this.message});

  @override
  List<Object> get props => [message];
}

